export const getWordsByCategory = (category: string): Word[] => {
  return jlptN5Words.filter(word => word.category === category);
};

export const getWordsByDifficulty = (difficulty: number): Word[] => {
  return jlptN5Words.filter(word => word.difficulty === difficulty);
};

export const getRandomWords = (count: number): Word[] => {
  const shuffled = [...jlptN5Words].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}; 